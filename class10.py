
# String Methods in Python?

# python provide a built-in methods that can use to alter and modify the string

# UpperCase():

# The uppercase convert all the string in Uppercase()

# Example:

# string1 ="adsmkfejiarh"
# print(string1.upper())
# # output:
# # ADSMKFEJIARH

# we can also find the length of string
a ="Bahadur khan"
print(len(a))
print("Hello",a.upper())


# lowerCase():

# The lower convert all the string in lowercase()

# Example:

string1 ="ASAHDSHWRUSTGDKDAJERHC"
print(string1.lower())
# output:
# ADSMKFEJIARH
a ="Bahadur khan"
print(len(a))
print("Hello",a.lower())
